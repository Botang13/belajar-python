#Belajar Operator Logika
#Digunakan untuk Tipe Data Boolean
#adalah AND dan OR

#Hanya ada 4 Kemungkinan
#Operator AND bernilai TRUE jika keduanya TRUE
print(True and True)
print(True and False)
print(False and True)
print(False and False)

#Operator OR bernilai TRUE jika salah satunya bernilai TRUE
print(True or True)
print(True or False)
print(False or True)
print(False or False)