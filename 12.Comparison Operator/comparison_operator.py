#Belajar Operator Perbandingan
#Menghasilkan Nilai Boolean
# > Lebih Dari 
# < Kurang Dari 
# >= Lebih Dari Sama Dengan 
# <= Kurang Dari Sama Dengan 
# == Sama Dengan
# != Tidak Sama Dengan
#

print(1 > 4)
print(1 < 4)
print(1 < 1)
print(1 <= 1)
print(5 == 5)
print(5 != 5)
print(5 != 10)
print(2 >= 3)
print(3 >= 2)
print("HER" == "HER")
print("HER" != "HER")